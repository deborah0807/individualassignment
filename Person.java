public class Person {

	private String Name;
	private String Address;
	private int Number;

	public Person() { }

	public void orang()
	{
		this.Name = Name;
		this.Address = Address;
		this.Number = Number;
	}

	public String getName()
	{
		return Name;
	}

	public String getAddress()
	{
		return Address;
	}

	public int getNumber()
	{
		return Number;
	}
}